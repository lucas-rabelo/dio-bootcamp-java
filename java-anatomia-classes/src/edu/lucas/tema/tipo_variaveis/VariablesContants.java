package edu.lucas.tema.tipo_variaveis;

public class VariablesContants {
    public static void main(String[] args) {
        /**
         * Example where the variable change
         */
        int numero = 1;
        numero = 2;
        System.out.print(numero);
        
        /**
         * Example of the constant
         * Is necessary to put "final" before on type for create a constant
         * and write cosntant in uppercasea
         */
        final double VALOR_DE_PI = 3.14;
        System.out.print(VALOR_DE_PI);
    }
}
